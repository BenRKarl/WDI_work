// Create a calculator in JavaScript.
// Support the following operations: addition, subtraction, multiplication, and division.


function add(a, b){
  return a+b;
}

function sub(a, b){
  return a-b;
}

function multi(a, b){
  return a*b;
}

function div(a, b){
  return a/b;
}

console.log("5 + 3 = " + add(5, 3));
console.log("5 - 3 = " + sub(5, 3));
console.log("5 * 3 = " + multi(5, 3));
console.log("5 / 3 = " + div(5, 3))

