// Given the following object, write a perimeter function in JavaScript that calculates the perimeter of triangle:

function perimeter(){
  var triangle = {
    sideA: 3,
    sideB: 4,
    sideC: 5
  };

  return triangle.sideA + triangle.sideB + triangle.sideC;
}

