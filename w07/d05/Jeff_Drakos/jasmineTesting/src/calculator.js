function add(number1, number2){
		var sum = (number1 + number2);
		return sum;
	
}

function subtract(number1, number2){
		var sum = (number1 - number2);
		return sum;
	
}

function multiply(number1, number2){
		var sum = (number1 * number2);
		return sum;
	
}
function divide(number1, number2){
		var sum = (number1 / number2);
		return sum;
	
}